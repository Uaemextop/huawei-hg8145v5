class LMSAClient:
    """Self-contained LMSA API client (all headers, body format, JWT rotation).

    RequestModel body (confirmed from decompiled RequestModel.cs):
        { client: {version}, language, windowsInfo, dparams: {..., guid} }

    Auth headers (confirmed from WebApiHttpRequest.RequestBase, decompiled):
        guid:          <GUID>
        Authorization: Bearer <JWT>

    JWT rotation: server echoes GUID in response header when it issues a new
    JWT in the Authorization response header.  Confirmed from source:
        if (Guid_header == GUID && Authorization_header != "")
            JWT_TOKEN = Authorization_header;
    """

    def __init__(
        self,
        guid: str,
        jwt: str,
        wust: str = "",
        base_url: str = LMSA_BASE_URL,
        verify_ssl: bool = True,
    ) -> None:
        self.guid      = guid.strip().lower()
        self._jwt      = jwt.strip().removeprefix("Bearer ").strip()
        self._wust     = wust.strip()
        self.base_url  = base_url.rstrip("/")
        self._verify   = verify_ssl
        self._lock     = threading.Lock()
        self._refreshing = False

        self._sess = requests.Session()
        self._sess.headers.update(_API_HEADERS)
        self._sess.verify = verify_ssl
        _retry = Retry(total=3, backoff_factor=0.5,
                       status_forcelist=(), allowed_methods={"POST"})
        self._sess.mount("https://", HTTPAdapter(max_retries=_retry))
        self._sess.mount("http://",  HTTPAdapter(max_retries=_retry))

    def current_jwt(self) -> str:
        return self._jwt

    def _auth_headers(self) -> dict[str, str]:
        """Per-request auth headers (guid + Authorization: Bearer <jwt>)."""
        hdrs: dict[str, str] = {"guid": self.guid}
        if self._jwt:
            hdrs["Authorization"] = f"Bearer {self._jwt}"
        return hdrs

    def _build_body(self, params: dict) -> dict:
        dp = dict(params)
        dp.setdefault("guid", self.guid)
        return {
            "client":      {"version": CLIENT_VERSION},
            "language":    "en-US",
            "windowsInfo": "Microsoft Windows 11 Pro, x64-based PC",
            "dparams":     dp,
        }

    def _rotate_jwt(self, resp: requests.Response) -> None:
        """Rotate JWT from response headers if server issues a new one."""
        if resp.headers.get("Guid", "").lower() == self.guid:
            new_auth = resp.headers.get("Authorization", "")
            if new_auth:
                new_jwt = new_auth.removeprefix("Bearer ").strip()
                if new_jwt and new_jwt != self._jwt:
                    self._jwt = new_jwt

    def post(self, endpoint: str, params: dict, *, _retrying: bool = False) -> Optional[dict]:
        """POST a RequestModel to *endpoint* and return parsed JSON."""
        body = self._build_body(params)
        url  = self.base_url + endpoint
        try:
            resp = self._sess.post(url, json=body,
                                   headers=self._auth_headers(), timeout=30)
        except requests.RequestException as exc:
            print(_warn(f"  [API] {endpoint}: {exc}"))
            return None

        self._rotate_jwt(resp)

        if resp.status_code == 401 and not _retrying:
            if self._refresh_jwt():
                return self.post(endpoint, params, _retrying=True)
            return None

        if resp.status_code != 200:
            print(_warn(f"  [API] {endpoint} → HTTP {resp.status_code}"))
            return None

        try:
            data = resp.json()
        except ValueError:
            return None

        code = data.get("code", "")
        if code in _AUTH_ERROR_CODES and not _retrying:
            print(_warn(f"  [API] Auth error {code!r} on {endpoint} — refreshing JWT …"))
            if self._refresh_jwt():
                return self.post(endpoint, params, _retrying=True)

        return data

    # ------------------------------------------------------------------
    # JWT refresh via WUST
    # ------------------------------------------------------------------

    def _refresh_jwt(self) -> bool:
        """Exchange WUST → fresh JWT via /user/lenovoIdLogin.jhtml.

        Confirmed from decompiled Software Fix.exe (LenovoIdLoginService):
          • author=false → NO guid/Authorization request headers
          • dparams: {wust: <WUST>, guid: <GUID>}
          • JWT arrives in "Authorization" response header when server echoes
            our GUID in the "Guid" response header
        """
        if not self._wust:
            print(_err(
                "  [auth] JWT expired and no WUST provided.\n"
                "         Add  wust = <your-wust>  to [auth] in config.ini"
            ))
            return False

        with self._lock:
            if self._refreshing:
                return bool(self._jwt)
            self._refreshing = True

        try:
            url = self.base_url + _EP_LOGIN_WUST
            body = {
                "client":      {"version": CLIENT_VERSION},
                "dparams":     {"wust": self._wust, "guid": self.guid},
                "language":    "en-US",
                "windowsInfo": "Microsoft Windows 11 Pro, x64-based PC",
            }
            print(_info("  [auth] Exchanging WUST for fresh JWT …"))
            r = self._sess.post(url, json=body, headers=_API_HEADERS, timeout=30)

            guid_resp = r.headers.get("Guid", "").lower()
            auth_resp = r.headers.get("Authorization", "")
            jwt: Optional[str] = None
            if guid_resp == self.guid and auth_resp:
                jwt = auth_resp.removeprefix("Bearer ").strip()
            elif auth_resp.startswith("Bearer "):
                jwt = auth_resp[len("Bearer "):]

            try:
                data = r.json()
            except ValueError:
                print(_err("  [auth] Non-JSON from lenovoIdLogin"))
                return False

            code = data.get("code", "")
            if code == "402":
                print(_err("  [auth] ✗ WUST expired — capture a fresh WUST"))
                return False
            if code not in (_CODE_OK, ""):
                print(_err(f"  [auth] ✗ lenovoIdLogin error {code!r}"))
                return False

            if not jwt:
                content = data.get("content") or data.get("data") or {}
                if isinstance(content, dict):
                    jwt = content.get("token") or content.get("jwt")

            if jwt:
                self._jwt = jwt
                print(_ok("  [auth] ✓ JWT refreshed via WUST"))
                return True

            print(_warn("  [auth] lenovoIdLogin ok but no JWT in response"))
            return False
        finally:
            with self._lock:
                self._refreshing = False

    # ------------------------------------------------------------------
    # initToken
    # ------------------------------------------------------------------

    def init_token(self) -> None:
        """Register GUID with the LMSA server (initToken.jhtml).

        Confirmed from ApiBaseService.InitTokenAsync (decompiled):
          Called once at session start to bind the GUID server-side.
          Non-fatal if it fails (endpoint returns 404 on some server versions).
        """
        data = self.post(_EP_INIT_TOKEN, {})
        if data and data.get("code") == _CODE_OK:
            print(_ok("  [auth] ✓ GUID registered (initToken)"))
        else:
            code = (data or {}).get("code", "no response")
            print(_dim(f"  [auth] initToken: {code!r} (non-fatal, continuing)"))

    # ------------------------------------------------------------------
    # Fresh pre-signed URL via getResource (for models without demogb)
    # ------------------------------------------------------------------

    def get_presigned_url(self, base_url: str) -> Optional[str]:
        """Get a fresh pre-signed S3 URL for a known model via getResource.

        Extracts the model number from *base_url*'s filename, queries
        getResource for multiple countries, and returns the first fresh
        pre-signed URL whose S3 path matches *base_url*.

        Confirmed working: getResource returns ?X-Amz-signed URLs directly.
        """
        fname = urllib.parse.urlparse(base_url).path.rsplit("/", 1)[-1]
        m = re.search(r"(XT\d{4}-?\d?)", fname, re.I)
        if not m:
            return None
        model_hint = m.group(1).upper()

        countries = ("Mexico", "US", "Brazil", "Argentina", "Colombia",
                     "India", "Germany", "UK", "China", "France", "Spain")
        target_path = urllib.parse.urlparse(base_url).path

        for country in countries:
            mn_data = self.post(_EP_GET_MODELS,
                                {"country": country, "category": "Phone"})
            if not mn_data or mn_data.get("code") != _CODE_OK:
                continue
            content = mn_data.get("content") or {}
            all_m = (content.get("models") or []) + (content.get("moreModels") or [])
            match = next(
                (m for m in all_m
                 if model_hint in (m.get("modelName") or "").upper()),
                None,
            )
            if not match:
                continue

            res_data = self.post(_EP_GET_RESOURCE, {
                "modelName":  match.get("modelName", ""),
                "marketName": match.get("marketName", ""),
                "country":    country,
            })
            if not res_data or res_data.get("code") != _CODE_OK:
                continue

            items = res_data.get("content") or []
            if not isinstance(items, list):
                items = [items] if items else []

            for item in items:
                for key in ("romResource", "toolResource", "otaResource",
                            "countryCodeResource"):
                    res = item.get(key)
                    if isinstance(res, dict):
                        uri = res.get("uri", "")
                        if uri and urllib.parse.urlparse(uri).path == target_path:
                            print(_info(
                                f"  [presign] ✓ Fresh URL via getResource "
                                f"({match.get('modelName')}, {country})"
                            ))
                            return uri
            break  # model found in this country — stop
        return None


# ============================================================================
# URL manifest parser — supports all crawler output formats
# ============================================================================

_URL_RE    = re.compile(r"https?://\S+", re.IGNORECASE)
_HEADER_RE = re.compile(r"-H\s+['\"]([^:\"']+):\s*([^\"']+)['\"]")


def _parse_urls_file(path: Path) -> list[tuple[str, str, dict[str, str]]]:
    """Load URLs from any manifest file the crawler produces.

    Returns list of  (url, filename, extra_headers)  triples.

    extra_headers is populated from download_links.txt curl -H entries:
    they contain the Authorization + guid from the crawl session that must
    be replaced with fresh credentials at download time.

    Supported formats
    -----------------
    1. ``<url>\\t<filename>``  — lmsa_firmware_urls.txt / lmsa_rom_urls.txt
    2. Curl commands           — download_links.txt  (headers extracted)
    3. Plain one-URL-per-line
    """
    if not path.is_file():
        print(_warn(f"  URL file not found: {path}"))
        return []

    triples: list[tuple[str, str, dict[str, str]]] = []
    with path.open("r", encoding="utf-8", errors="replace") as fh:
        lines = fh.readlines()

    i = 0
    while i < len(lines):
        line = lines[i].strip()
        i += 1

        if not line or line.startswith("#"):
            continue

        # ── curl block from download_links.txt ───────────────────────────
        if line.startswith("curl "):
            # Collect continuation lines
            block = line
            while block.endswith("\\") and i < len(lines):
                block = block[:-1] + lines[i].strip()
                i += 1

            # Extract URL (last quoted arg)
            urls_in_block = _URL_RE.findall(block)
            if not urls_in_block:
                continue
            url = urls_in_block[-1].rstrip("'\"")

            # Extract all -H headers (will be replaced with fresh jwt/guid)
            hdrs: dict[str, str] = {}
            for name, val in _HEADER_RE.findall(block):
                hdrs[name.strip()] = val.strip()

            fname = _filename_from_url(url)
            triples.append((url, fname, hdrs))
            continue

        # ── Tab-separated: URL <TAB> filename ────────────────────────────
        if "\t" in line:
            url, _, fname = line.partition("\t")
            url   = url.strip()
            fname = fname.strip() or _filename_from_url(url)
            if url.startswith("http"):
                triples.append((url, fname, {}))
            continue

        # ── Plain URL ─────────────────────────────────────────────────────
        for m in _URL_RE.finditer(line):
            url = m.group(0).rstrip("'\"\\,;")
            if url:
                triples.append((url, _filename_from_url(url), {}))

    # Deduplicate by base URL preserving order
    seen: set[str] = set()
    unique: list[tuple[str, str, dict[str, str]]] = []
    for url, fname, hdrs in triples:
        b = _strip_qs(url)
        if b not in seen:
            seen.add(b)
            unique.append((url, fname, hdrs))
    return unique


