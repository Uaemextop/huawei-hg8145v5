# ============================================================================
# URL helpers
# ============================================================================

def _host(url: str) -> str:
    return urllib.parse.urlparse(url).netloc.lower()

def _is_direct_auth(url: str) -> bool:
    """True for rsddownload-secure.lenovo.com URLs WITHOUT ?X-Amz- signing.

    These require  Authorization: Bearer <jwt>  +  guid: <guid>  headers.
    Confirmed from download_links.txt generated by crawler (2026-03-04).
    """
    if _host(url) not in _S3_AUTH_HOSTS:
        return False
    return "X-Amz-" not in url

def _is_presigned(url: str) -> bool:
    """True for AWS pre-signed URLs (contain ?X-Amz-Algorithm=…).

    Downloaded directly — no auth headers needed; credentials are in the URL.
    Obtained fresh via getResource.jhtml.
    """
    return _host(url) in _S3_AUTH_HOSTS and "X-Amz-" in url

def _strip_qs(url: str) -> str:
    p = urllib.parse.urlparse(url)
    return urllib.parse.urlunparse(p._replace(query="", fragment=""))

def _filename_from_url(url: str) -> str:
    path = urllib.parse.urlparse(url).path
    name = path.rstrip("/").rsplit("/", 1)[-1]
    return urllib.parse.unquote(name) if name else "download"


