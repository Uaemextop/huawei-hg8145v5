#!/usr/bin/env python3
"""LMSA Firmware Downloader — standalone, self-contained tool.

Downloads firmware files from lsa.lenovo.com / rsddownload-secure.lenovo.com
using a GUID + JWT captured from a live LMSA session (HTTPToolkit / Charles).

Two URL types are handled automatically:

  1. rsddownload-secure.lenovo.com (direct auth):
       GET https://rsddownload-secure.lenovo.com/<file.zip>
       Headers: Authorization: Bearer <jwt>  +  guid: <guid>
       → These are the URLs in download_links.txt.  A fresh JWT is required.

  2. Pre-signed S3 URLs (from getResource.jhtml):
       GET https://rsddownload-secure.lenovo.com/<file.zip>?X-Amz-Algorithm=…
       → No auth headers needed; URL itself contains AWS credentials.
       → The tool fetches fresh pre-signed URLs via getResource.jhtml.

When the JWT expires the tool exchanges the WUST (if provided) for a fresh
JWT automatically.

Usage::

    python downloader.py [--config config.ini] [--dry-run]

Requirements: Python 3.9+  and  pip install requests
"""

