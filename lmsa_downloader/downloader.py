class Downloader:
    """Download firmware files with automatic auth-header injection.

    Two download strategies (selected per URL):

    A) Direct auth  (rsddownload-secure.lenovo.com, no ?X-Amz- in URL):
         GET <url>  with  Authorization: Bearer <fresh_jwt>  +  guid: <guid>
         Confirmed from download_links.txt generated by crawler.

    B) Pre-signed   (any host, URL contains ?X-Amz-Algorithm=…):
         GET <url>  — credentials embedded in URL, no headers needed.
         If URL expired (403) → call getResource.jhtml to get fresh URL.
    """

    def __init__(
        self,
        client: LMSAClient,
        output_dir: Path,
        workers: int = 4,
        retries: int = 3,
        resume:  bool = True,
        verify_ssl: bool = True,
    ) -> None:
        self._client  = client
        self._out     = output_dir
        self._workers = max(1, workers)
        self._retries = max(1, retries)
        self._resume  = resume

        self._sess = requests.Session()
        self._sess.verify = verify_ssl
        dl_retry = Retry(total=retries, backoff_factor=1.0,
                         status_forcelist={500, 502, 503, 504},
                         allowed_methods={"GET", "HEAD"})
        self._sess.mount("https://", HTTPAdapter(max_retries=dl_retry))
        self._sess.mount("http://",  HTTPAdapter(max_retries=dl_retry))

    def _dl_headers(self, url: str) -> dict[str, str]:
        """Build download headers for *url*.

        For direct-auth S3 URLs → inject fresh JWT + guid.
        For pre-signed / public URLs → minimal headers only.
        """
        hdrs = dict(_DL_HEADERS)
        if _is_direct_auth(url):
            hdrs["Authorization"] = f"Bearer {self._client.current_jwt()}"
            hdrs["guid"]          = self._client.guid
        return hdrs

    def _resolve_url(self, url: str) -> str:
        """Return a ready-to-download URL.

        • Direct-auth (rsddownload-secure, no ?X-Amz-): URL stays the same,
          fresh JWT will be injected in headers at download time.
        • Pre-signed (URL has ?X-Amz-): URL already has credentials, but if
          it expired (403) we fetch a fresh URL via getResource.jhtml.
        • Public (download.lenovo.com, etc.): no change.
        """
        if _is_direct_auth(url):
            return url  # auth via headers, not URL params
        if _is_presigned(url):
            # will get fresh URL on 403 in _download_one
            return url
        return url  # public

    def _download_one(
        self,
        url: str,
        name: str,
        extra_hdrs: dict[str, str],
    ) -> tuple[str, bool, str]:
        """Download *url* → output_dir/name.  Returns (name, ok, message)."""
        dest = self._out / name
        dest.parent.mkdir(parents=True, exist_ok=True)

        dl_url = self._resolve_url(url)

        # Resume: check partial file
        resume_pos = 0
        if self._resume and dest.exists() and dest.stat().st_size > 0:
            resume_pos = dest.stat().st_size
            try:
                h = dict(self._dl_headers(dl_url))
                head = self._sess.head(dl_url, headers=h,
                                       timeout=15, allow_redirects=True)
                total = int(head.headers.get("Content-Length", 0))
                if total and resume_pos >= total:
                    return name, True, "already complete"
            except Exception:
                resume_pos = 0

        downloaded = resume_pos

        for attempt in range(1, self._retries + 1):
            hdrs = dict(self._dl_headers(dl_url))
            if resume_pos:
                hdrs["Range"] = f"bytes={resume_pos}-"
            mode = "ab" if resume_pos else "wb"

            try:
                with self._sess.get(
                    dl_url, headers=hdrs, stream=True,
                    timeout=(15, 600), allow_redirects=True,
                ) as resp:
                    if resp.status_code == 416:
                        return name, True, "already complete (416)"

                    # 403 on pre-signed URL → try fresh URL from getResource
                    if resp.status_code == 403 and _is_presigned(dl_url):
                        if attempt < self._retries:
                            fresh = self._client.get_presigned_url(_strip_qs(dl_url))
                            if fresh:
                                dl_url = fresh
                            continue

                    # 403 on direct-auth URL → JWT may be expired, refresh
                    if resp.status_code == 403 and _is_direct_auth(dl_url):
                        if attempt < self._retries and self._client._wust:
                            self._client._refresh_jwt()
                            continue
                        return name, False, (
                            "HTTP 403 — JWT expired. "
                            "Capture fresh GUID+JWT from HTTPToolkit and update config.ini"
                        )

                    if resp.status_code not in (200, 206):
                        msg = f"HTTP {resp.status_code}"
                        if attempt < self._retries:
                            time.sleep(2 ** attempt)
                            continue
                        return name, False, msg

                    with dest.open(mode) as fh:
                        for chunk in resp.iter_content(1 << 16):
                            if chunk:
                                fh.write(chunk)
                                downloaded += len(chunk)

                    return name, True, f"{downloaded / 1_048_576:.1f} MB"

            except requests.RequestException as exc:
                if attempt < self._retries:
                    time.sleep(2 ** attempt)
                else:
                    return name, False, str(exc)

        return name, False, "max retries exceeded"

    def run(self, triples: list[tuple[str, str, dict[str, str]]]) -> None:
        """Download all  (url, filename, extra_headers)  triples."""
        self._out.mkdir(parents=True, exist_ok=True)
        total = len(triples)
        ok_n  = 0
        failed: list[str] = []

        print(f"\n{_info('Downloading')} {total} file(s) → {self._out}/")
        print(f"Workers: {self._workers}  Retries: {self._retries}  Resume: {self._resume}\n")

        done = 0
        with ThreadPoolExecutor(max_workers=self._workers) as pool:
            futs = {
                pool.submit(self._download_one, u, n, h): n
                for u, n, h in triples
            }
            for fut in as_completed(futs):
                done += 1
                name, success, msg = fut.result()
                tag = f"[{done:>4}/{total}]"
                if success:
                    ok_n += 1
                    print(f"{tag} {_ok('✓')} {name}  {_dim(f'({msg})')}")
                else:
                    failed.append(name)
                    print(f"{tag} {_err('✗')} {name}  — {_err(msg)}")

        print(f"\n{'─'*62}")
        print(f"Result: {_ok(str(ok_n))} succeeded  /  "
              f"{_err(str(len(failed)))} failed  /  {total} total")
        if failed:
            print(_warn("\nFailed files:"))
            for f in failed:
                print(f"  • {f}")
        print()


# ============================================================================
# Config helpers
