# Entry point
# ============================================================================

def main() -> None:
    ap = argparse.ArgumentParser(
        description="LMSA Firmware Downloader — downloads firmware using fresh GUID+JWT.",
        formatter_class=argparse.ArgumentDefaultsHelpFormatter,
    )
    ap.add_argument("--config", "-c", default="config.ini",
                    help="Path to config.ini")
    ap.add_argument("--dry-run", action="store_true",
                    help="Print resolved info without downloading")
    ap.add_argument("--output", "-o", default="",
                    help="Override output directory")
    ap.add_argument("--workers", "-w", type=int, default=0,
                    help="Override parallel worker count (0 = use config)")
    args = ap.parse_args()

    config_path = Path(args.config).resolve()
    if not config_path.is_file():
        sys.exit(f"ERROR: config file not found: {config_path}")

    cfg        = _load_config(config_path)
    config_dir = config_path.parent

    # ── Auth ─────────────────────────────────────────────────────────────────
    lmsa_jwt_raw = _get(cfg, "auth", "lmsa_jwt")
    guid_raw     = _get(cfg, "auth", "guid")
    jwt_raw      = _get(cfg, "auth", "jwt")
    wust_raw     = _get(cfg, "auth", "wust")

    # Support GUID:JWT combined format (same as LMSA_JWT workflow input)
    if lmsa_jwt_raw and ":" in lmsa_jwt_raw:
        guid_raw, _, jwt_raw = lmsa_jwt_raw.partition(":")

    def _is_set(v: str) -> bool:
        return bool(v) and not v.upper().startswith("PASTE_") \
               and not v.upper().startswith("YOUR_")

    if not _is_set(guid_raw) or not _is_set(jwt_raw):
        sys.exit(
            "ERROR: GUID and JWT are required.\n\n"
            "Option A — combined GUID:JWT format:\n"
            "  [auth]\n"
            "  lmsa_jwt = 3adf1304-8a70-4352-b687-8eddcef6b7d1:dcwyMSw…\n\n"
            "Option B — separate fields:\n"
            "  [auth]\n"
            "  guid = 3adf1304-8a70-4352-b687-8eddcef6b7d1\n"
            "  jwt  = dcwyMSw…\n\n"
            "Add  wust = …  to enable automatic JWT refresh when it expires.\n\n"
            "Capture GUID and JWT from a running LMSA session via HTTPToolkit."
        )

    guid       = guid_raw.strip()
    jwt        = jwt_raw.strip()
    wust       = wust_raw.strip()
    base_url   = _get(cfg, "auth", "base_url", LMSA_BASE_URL)
    verify_ssl = cfg.getboolean("auth", "verify_ssl", fallback=True)

    # ── Settings ──────────────────────────────────────────────────────────────
    output_dir = Path(
        args.output or _get(cfg, "settings", "output_dir", "downloads")
    )
    if not output_dir.is_absolute():
        output_dir = config_dir / output_dir

    workers = args.workers or cfg.getint("settings", "workers", fallback=4)
    retries = cfg.getint("settings", "retries", fallback=3)
    resume  = cfg.getboolean("settings", "resume", fallback=True)

    # ── URLs ──────────────────────────────────────────────────────────────────
    triples: list[tuple[str, str, dict[str, str]]] = []

    urls_file_str = _get(cfg, "urls", "urls_file")
    if urls_file_str:
        uf = Path(urls_file_str)
        if not uf.is_absolute():
            uf = config_dir / uf
        loaded = _parse_urls_file(uf)
        print(f"Loaded {len(loaded)} URL(s) from {uf.name}" if loaded
              else _warn(f"No URLs found in {uf}"))
        triples.extend(loaded)

    # Inline urls: url_1 = …, url_2 = …
    if cfg.has_section("urls"):
        for key, val in cfg.items("urls"):
            if key.startswith("url") and key != "urls_file":
                url = val.strip()
                if url.startswith("http"):
                    triples.append((url, _filename_from_url(url), {}))

    if not triples:
        sys.exit(
            "ERROR: No URLs to download.\n"
            "Set  [urls] urls_file = lmsa_firmware_urls.txt  in config.ini\n"
            "  or set  [urls] urls_file = download_links.txt\n"
            "  or add  url_1 = https://…"
        )

    # Deduplicate
    seen_b: set[str] = set()
    unique: list[tuple[str, str, dict[str, str]]] = []
    for u, n, h in triples:
        b = _strip_qs(u)
        if b not in seen_b:
            seen_b.add(b)
            unique.append((u, n, h))
    triples = unique

    # ── Banner ────────────────────────────────────────────────────────────────
    s3_auth_count   = sum(1 for u, _, _ in triples if _is_direct_auth(u))
    presigned_count = sum(1 for u, _, _ in triples if _is_presigned(u))
    public_count    = len(triples) - s3_auth_count - presigned_count

    print(f"\n{'═'*62}")
    print(f"  LMSA Firmware Downloader")
    print(f"{'─'*62}")
    print(f"  Config  : {config_path}")
    print(f"  API     : {base_url}")
    print(f"  GUID    : {guid}")
    print(f"  JWT     : {jwt[:20]}…")
    if wust:
        print(f"  WUST    : {wust[:20]}…  (JWT auto-refresh enabled)")
    print(f"  Output  : {output_dir}")
    print(f"  URLs    : {len(triples)}  "
          f"(direct-auth:{s3_auth_count}  presigned:{presigned_count}  public:{public_count})")
    if args.dry_run:
        print(f"  Mode    : {_warn('DRY RUN — no files will be downloaded')}")
    print(f"{'═'*62}\n")

    # ── Build client and register GUID ────────────────────────────────────────
    client = LMSAClient(guid=guid, jwt=jwt, wust=wust,
                        base_url=base_url, verify_ssl=verify_ssl)
    client.init_token()

    # ── Dry run ───────────────────────────────────────────────────────────────
    if args.dry_run:
        print(_info("[dry-run] URL analysis:"))
        for url, name, hdrs in triples:
            if _is_direct_auth(url):
                kind = _info("direct-auth")
                note = "needs fresh JWT+GUID in headers"
            elif _is_presigned(url):
                kind = _ok("pre-signed")
                note = "credentials in URL"
            else:
                kind = _ok("public")
                note = "no auth needed"
            print(f"  [{kind}] {name}")
            print(f"           {url[:80]}…" if len(url) > 80 else f"           {url}")
            print(f"           {_dim(note)}")
        return

    # ── Download ──────────────────────────────────────────────────────────────
    Downloader(
        client=client,
        output_dir=output_dir,
        workers=workers,
        retries=retries,
        resume=resume,
        verify_ssl=verify_ssl,
    ).run(triples)


if __name__ == "__main__":
    main()
