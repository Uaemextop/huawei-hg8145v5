# ---------------------------------------------------------------------------


class WordPressDetector(BaseDetector):
    """Detect WordPress-powered websites."""

    name = "wordpress"

    _WP_SIGNATURES = (
        "/wp-content/",
        "/wp-includes/",
        "/wp-json/",
        "wp-emoji-release.min.js",
        'name="generator" content="WordPress',
    )

    _WP_HEADER_LINKS = (
        "wp-json",
        "xmlrpc.php",
    )

    def detect(
        self,
        url: str,
        status_code: int,
        headers: dict,
        body: str,
    ) -> dict | None:
        # Check Link header
        link_header = headers.get("link", "")
        for sig in self._WP_HEADER_LINKS:
            if sig in link_header:
                return {"type": "wordpress", "method": "header"}

        # Check body signatures
        if body:
            for sig in self._WP_SIGNATURES:
                if sig in body:
                    return {"type": "wordpress", "method": "body",
                            "signature": sig}

        return None


# ---------------------------------------------------------------------------
# detect_all  – registry that runs every detector
# ---------------------------------------------------------------------------

ALL_DETECTORS: list[BaseDetector] = [
    CloudflareDetector(),
    SiteGroundDetector(),
    WAFDetector(),
    Soft404Detector(),
    WordPressDetector(),
]


def detect_all(
    url: str,
    status_code: int,
    headers: dict,
    body: str,
) -> list[dict]:
    """Run every registered detector and return a list of matches."""
    results: list[dict] = []
    for detector in ALL_DETECTORS:
        try:
            result = detector.detect(url, status_code, headers, body)
            if result is not None:
                results.append(result)
        except Exception:
            pass
    return results


