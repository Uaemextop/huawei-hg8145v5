"""
Generic WAF (Web Application Firewall) detection.

Scans the response body for signatures of known WAF / security products:
Cloudflare, Wordfence, Sucuri, ModSecurity, Imperva / Incapsula, Akamai,
Shield Security, iThemes Security, SiteGround, and generic CAPTCHA tokens.

The :data:`WAF_SIGNATURES` dictionary is the canonical source of all
signatures used by the detection engine.
"""

